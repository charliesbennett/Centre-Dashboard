export function getProgrammesForCentre(centreName) {
  if (!centreName) return [];
  const search = centreName.toLowerCase();
  return PROGRAMME_DATA.filter(p => {
    const pc = p.centre.toLowerCase();
    // Match on key words from centre name
    const words = search.split(/[,]/).map(w => w.trim()).filter(w => w.length > 3);
    return words.some(w => pc.includes(w)) || pc.includes(search);
  });
}
